#!/usr/bin/env node
/**
 * falda-authproxy.mjs — authenticating reverse proxy in front of the FALDA gateway.
 *
 * WHY: the FALDA gateway reads `tenant` straight from the JSON body with no auth
 * (gateway.ts: `tenant = b.tenant ?? DEFAULT_TENANT`). Any reachable caller can
 * spoof any tenant. This proxy is the trust boundary:
 *
 *   1. Requires `Authorization: Bearer <token>` on every data/pool route.
 *   2. Maps the token -> a fixed tenant (server-side table). Unknown/missing -> 401.
 *   3. OVERWRITES body.tenant with the token's tenant before forwarding. A client
 *      can put anything in the tenant field; it is ignored. This is what closes
 *      the spoofing gap.
 *   4. Forwards to the loopback-only gateway. The gateway never faces the network;
 *      only this proxy does.
 *
 * Tokens are compared by constant-time hash. The token map lives in a 0600 JSON
 * file (FALDA_AUTH_TOKENS, default ~/.falda/authproxy.tokens.json):
 *     { "<sha256(token)>": "tenant", ... }
 * Tokens are generated by falda-authproxy-token.mjs (never typed by hand), so the
 * raw secret is shown once and only the hash is persisted.
 *
 * Env:
 *   FALDA_AUTH_PORT     proxy listen port         (default 8078)
 *   FALDA_AUTH_HOST     proxy bind addr           (default 127.0.0.1)
 *   FALDA_GATEWAY_URL   upstream gateway          (default http://127.0.0.1:8077)
 *   FALDA_AUTH_TOKENS   token map path            (default ~/.falda/authproxy.tokens.json)
 */
import { createServer, request as httpRequest } from "node:http";
import { readFileSync } from "node:fs";
import { createHash, timingSafeEqual } from "node:crypto";
import { homedir } from "node:os";
import { join } from "node:path";

const PORT = parseInt(process.env.FALDA_AUTH_PORT ?? "8078", 10);
const HOST = process.env.FALDA_AUTH_HOST ?? "127.0.0.1";
const GATEWAY = new URL(process.env.FALDA_GATEWAY_URL ?? "http://127.0.0.1:8077");
const TOKENS_PATH = process.env.FALDA_AUTH_TOKENS ?? join(homedir(), ".falda", "authproxy.tokens.json");

// Routes that must never accept a client-supplied tenant. Pool admin routes are
// included: declaring/granting on behalf of a spoofed tenant would be a privilege leak.
const PROTECTED_PREFIXES = ["/stream/", "/atoms/", "/scenes/", "/core/", "/pools/"];

const sha256 = (s) => createHash("sha256").update(s, "utf8").digest("hex");

function loadTokens() {
  try {
    const raw = readFileSync(TOKENS_PATH, "utf8");
    const map = JSON.parse(raw);
    if (map && typeof map === "object") return map;
  } catch (e) {
    process.stderr.write(`[authproxy] WARN cannot read token map ${TOKENS_PATH}: ${e?.message ?? e}\n`);
  }
  return {};
}

// Constant-time lookup: hash the presented token, compare against every known
// hash with timingSafeEqual, return the matched tenant (or null).
function tenantForToken(token, tokenMap) {
  if (!token) return null;
  const presented = Buffer.from(sha256(token), "hex");
  let matched = null;
  for (const [hash, tenant] of Object.entries(tokenMap)) {
    let known;
    try { known = Buffer.from(hash, "hex"); } catch { continue; }
    if (known.length === presented.length && timingSafeEqual(known, presented)) matched = tenant;
  }
  return matched;
}

function send(res, code, obj) {
  const body = JSON.stringify(obj);
  res.writeHead(code, { "content-type": "application/json", "content-length": Buffer.byteLength(body) });
  res.end(body);
}

const server = createServer((req, res) => {
  const url = new URL(req.url, `http://${HOST}:${PORT}`);
  const path = url.pathname;

  // Health passes through unauthenticated (GET /healthz) — no tenant, no data.
  if (path === "/healthz" && req.method === "GET") {
    const gwReq = httpRequest(
      { hostname: GATEWAY.hostname, port: GATEWAY.port, path: "/healthz", method: "GET" },
      (gwRes) => { res.writeHead(gwRes.statusCode ?? 502, gwRes.headers); gwRes.pipe(res); }
    );
    gwReq.on("error", () => send(res, 502, { error: "gateway_unreachable" }));
    gwReq.end();
    return;
  }

  const isProtected = PROTECTED_PREFIXES.some((p) => path.startsWith(p));
  if (!isProtected) return send(res, 404, { error: "not_found", path });

  // ─── AUTH: bearer token -> tenant ───────────────────────────────────────────
  const auth = req.headers["authorization"] ?? "";
  const m = /^Bearer\s+(.+)$/i.exec(auth);
  const token = m ? m[1].trim() : null;
  const tenant = tenantForToken(token, loadTokens());
  if (!tenant) return send(res, 401, { error: "unauthorized", detail: "missing or invalid bearer token" });

  // ─── Read body, OVERWRITE tenant, forward ────────────────────────────────────
  const chunks = [];
  req.on("data", (c) => chunks.push(c));
  req.on("end", () => {
    let body = {};
    if (chunks.length) {
      try { body = JSON.parse(Buffer.concat(chunks).toString("utf8")); }
      catch { return send(res, 400, { error: "bad_json" }); }
    }
    // The trust boundary: whatever the client put in `tenant` is discarded and
    // replaced with the token-bound identity. Pool routes that name OTHER tenants
    // in their members map are still allowed (that's how you grant peers), but the
    // ACTING tenant is always the authenticated one.
    body.tenant = tenant;
    const payload = Buffer.from(JSON.stringify(body), "utf8");

    const gwReq = httpRequest(
      {
        hostname: GATEWAY.hostname, port: GATEWAY.port, path, method: "POST",
        headers: { "content-type": "application/json", "content-length": payload.length },
      },
      (gwRes) => { res.writeHead(gwRes.statusCode ?? 502, gwRes.headers); gwRes.pipe(res); }
    );
    gwReq.on("error", () => send(res, 502, { error: "gateway_unreachable" }));
    gwReq.end(payload);
  });
});

server.listen(PORT, HOST, () =>
  console.log(`[authproxy] listening ${HOST}:${PORT} -> gateway ${GATEWAY.origin} (tokens=${TOKENS_PATH})`)
);
